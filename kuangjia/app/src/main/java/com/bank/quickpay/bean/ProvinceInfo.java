package com.bank.quickpay.bean;

import java.util.ArrayList;

public class ProvinceInfo {	
	String   provinceCode;
	String   provinceName;
	ArrayList<CityInfo>  cityslist;
	
	public ProvinceInfo(){};
	public ProvinceInfo(String provinceCode, String provinceName,
                        ArrayList<CityInfo> cityslist) {
		super();
		this.provinceCode = provinceCode;
		this.provinceName = provinceName;
		this.cityslist = cityslist;
	}
	
	public String getProvinceCode() {
		return provinceCode;
	}
	public String getProvinceName() {
		return provinceName;
	}
	public ArrayList<CityInfo> getCityslist() {
		return cityslist;
	}
	public void setProvinceCode(String provinceCode) {
		this.provinceCode = provinceCode;
	}
	public void setProvinceName(String provinceName) {
		this.provinceName = provinceName;
	}
	public void setCityslist(ArrayList<CityInfo> cityslist) {
		this.cityslist = cityslist;
	}


}
